<h2 text-color="Blue">STOCKS: An Overview</h2>
<font face="Comic Sans">
You’ve probably heard a popular definition of what a stock is: “A stock is a share in the ownership of a company. Stock represents a claim on the company's assets and earnings. As you acquire more stock, your ownership stake in the company becomes greater.” Unfortunately, this definition is incorrect in some key ways.<br><br>

To start with, stock holders do not own corporations; they own shares issued by corporations. But corporations are a special type of organization because the law treats them as legal persons. In other words, corporations file taxes, can borrow, can own property, can be sued, etc. The idea that a corporation is a “person” means that the corporation owns its own assets. A corporate office full of chairs and tables belong to the corporation, and not to the shareholders.<br><br>

This distinction is important because corporate property is legally separated from the property of shareholders, which limits the liability of both the corporation and the shareholder. If the corporation goes bankrupt, a judge may order all of its assets sold – but your personal assets are not at risk. The court cannot even force you to sell your shares, although the value of your shares will have fallen drastically. Likewise, if a major shareholder goes bankrupt, she cannot sell the company’s assets to pay off her creditors.<br><br>

What shareholders own are shares issued by the corporation; and the corporation owns the assets. So if you own 33% of the shares of a company, it is incorrect to assert that you own one-third of that company; it is instead correct to state that you own 100% of one-third of the company’s shares. Shareholders cannot do as they please with a corporation or its assets. A shareholder can’t walk out with a chair because the corporation owns that chair, not the shareholder. This is known as the “separation of ownership and control.”<br><br>

So what good are shares, then, if they aren’t actually the ownership rights we think they are? Owning stock gives you the right to vote in shareholder meetings, receive dividends (which are the company’s profits) if and when they are distributed, and it gives you the right to sell your shares to somebody else.<br><br>

Stocks – sometimes referred to as equity or equities – are issued by companies to raise capital in order to grow the business or undertake new projects. There are important distinctions between whether somebody buys shares directly from the company when it issues them (in the primary market) or from another shareholder (on the secondary market). When the corporation issues shares, it does so in return for money.<br><br>

